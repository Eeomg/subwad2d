package subway2d;

/**
 *
 * @author AFC
 */
public class Position {
   private int x = 300 , y = 400;

    public Position() {
    }
   
    public Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
